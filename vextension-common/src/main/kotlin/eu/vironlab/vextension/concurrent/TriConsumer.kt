package eu.vironlab.vextension.concurrent

import java.util.*

@FunctionalInterface
/**
 * This class allows to use a Consumer with 3 different References
 *
 * @param <F> First - Is the first value of the Consumer
 * @param <S> Second - Is the second value of the Consumer
 * @param <T> Third - Is the third value of the Consumer
</T></S></F> */
fun interface TriConsumer<F, S, T> {
    /**
     * Accept the Consumer
     * @param first is the first Value entered to the Consumer
     * @param second is the second Value entered to the Consumer
     * @param third is the third Value entered to the Consumer
     */
    fun accept(first: F, second: S, third: T)

    /**
     * Execute an Consumer after finish the main Consumer
     * @param after is the Consumer to accept after
     * @return the the accepted Consumer
     */
    fun andThen(after: TriConsumer<in F, in S, in T>): TriConsumer<F, S, T>? {
        Objects.requireNonNull(after)
        return TriConsumer { f: F, s: S, r: T ->
            accept(f, s, r)
            after.accept(f, s, r)
        }
    }
}